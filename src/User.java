
public abstract class User {
	private String id;
	private String password;
	
	public String getID() {
		return this.id;
	}
	
	public void setID(String id) {
		this.id=id;
	}
	
	public String getPassword() {
		return this.password;
	}
	
	public void setPassword(String password) {
		this.password=password;
	}
	
}
